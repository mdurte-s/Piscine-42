void	fill_grid(char grid[4][4])
{
	int	r;
	int	c;

	r = 0;
	while (r < 4)
	{
		c = 0;
		while (c < 4)
		{
			grid[r][c] = '1' + (r + c) % 4;
			c++;
		}
		r++;
	}
}
